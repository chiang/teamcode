public class TeamcodeSecurityException extends TeamcodeException {

    public TeamcodeSecurityException() {
        super();
    }

    public TeamcodeSecurityException(String s) {
        super(s);
    }

    public TeamcodeSecurityException(Throwable t) {
        super(t);
    }

    public TeamcodeSecurityException(String s, Throwable t) {
        super(s, t);
    }

}
